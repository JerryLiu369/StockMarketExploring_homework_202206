qreal dotmut(const QList<qreal> &a,const QList<qreal> &b)
{
    qreal res=qQNaN();
    if(a.length()!=b.length())return res;
    res=0;
    auto itera=a.begin();
    auto iterb=b.begin();
    while (itera!=a.end()) {
        res+=(*itera)*(*iterb);
        itera++;
        iterb++;
    }
    return res;
}

